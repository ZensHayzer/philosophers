#include "../../includes/philo.h"

int	ft_atoi(char *str)	{
	int	dest;
	int	i;
	
	dest = 0;
	i = 0;
	while (str[i])
	{
		dest = dest * 10 + (str[i] - '0');
		i++;
	}
	return (dest);
}